import serial
import time

ser = serial.Serial('/dev/ttyUSB0', 115200)

print("Connected to Kobuki")

cmd = bytearray([
0xAA,0x55,
0x06,
0x01,0x04,
0x64,0x00,
0x00,0x00
])

ser.write(cmd)

time.sleep(3)

stop = bytearray([
0xAA,0x55,
0x06,
0x01,0x04,
0x00,0x00,
0x00,0x00
])

ser.write(stop)

print("Stopped")

